package com.maoqi.listen.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

/**
 * Created by maoqi on 2017/6/15.
 */

public class SearchListAdapter extends FragmentPagerAdapter {
    private String[] titles = {"网易云","虾米","QQ音乐"};
    private List<Fragment> data;

    public SearchListAdapter(FragmentManager fm, List<Fragment> data) {
        super(fm);
        this.data = data;
    }

    @Override
    public Fragment getItem(int position) {
        return data.get(position);
    }

    @Override
    public int getCount() {
        return titles.length;
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return titles[position];
    }
}
